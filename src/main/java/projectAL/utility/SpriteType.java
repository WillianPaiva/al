package projectAL.utility;

public class SpriteType {
  private String type;
	private int length;

	public SpriteType(String t, int i) {
    this.type = t;
    this.length = i;
	}

	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}


	/**
	 * @return the length
	 */
	public int getLength() {
		return length;
	}


}
